namespace LogicalModel.Implementation
{
    /***************************************************************************/

    using API;

    /***************************************************************************/

    public class Port : IPort
    {
        /***************************************************************************/

        public Port()
        {
            m_logicValue = API.LogicValue.Enum.Unknown;
        }

        /***************************************************************************/

        public LogicValue.Enum LogicValue
        {
            get
            {
                return m_logicValue;
            }
         
            set
            {
                m_logicValue = value;
            }
        }

        /***************************************************************************/

        private LogicValue.Enum m_logicValue;

        /***************************************************************************/
    }
}

